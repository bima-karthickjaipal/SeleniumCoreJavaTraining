package Sep14Training;

public class IncrementDecrementdemo {

    public static void main(String[] args) {
        int i=10;
        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);

        int j=10;
        System.out.println(j);
        System.out.println(--j);
        System.out.println(j);
        System.out.println(j--);
        System.out.println(j);
    }
}
